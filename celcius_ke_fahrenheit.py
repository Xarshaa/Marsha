#meminta pengguna untuk memasukkan suhu ke dalam derajat celcius
celcius = float(input("memasukkan suhu dalam celcius:"))

#menghitung konversi suhu ke fahreinheit
fahrenheit = (celcius * 9/5)+ 32

#menampilkan hasil konversi
print("f{celcius} derajat celcius sama dengan {fahrenheit} derajat fahrenheit :" , fahrenheit)